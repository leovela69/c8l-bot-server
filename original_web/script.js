// Submit handler (Login or Register)
function handleAuthSubmit() {
    if (typeof firebase === 'undefined') return;
    const email = document.getElementById('auth-email').value;
    const password = document.getElementById('auth-password').value;

    if (!email || !password) {
        showNotification(currentLang === 'es' ? 'Por favor ingresa todos los campos.' : 'Please enter all fields.');
        return;
    }

    if (password.length < 6) {
        showNotification(currentLang === 'es' ? 'La contraseña debe tener al menos 6 caracteres.' : 'Password must be at least 6 characters.');
        return;
    }

    if (authTabState === 'login') {
        // Sign In
        firebase.auth().signInWithEmailAndPassword(email, password)
            .then((userCredential) => {
                showNotification(currentLang === 'es' ? 'Sesión iniciada con éxito.' : 'Logged in successfully.');
            })
            .catch((error) => {
                console.error("Login error:", error);
                if (error.code === 'auth/user-not-found' || error.code === 'auth/wrong-password' || error.code === 'auth/invalid-credential') {
                    showNotification(currentLang === 'es' ? 'Credenciales incorrectas o usuario no encontrado.' : 'Incorrect credentials or user not found.');
                } else {
                    showNotification(error.message);
                }
            });
    } else {
        // Create User (Register)
        firebase.auth().createUserWithEmailAndPassword(email, password)
            .then((userCredential) => {
                showNotification(currentLang === 'es' ? 'Registro exitoso. ¡Bienvenido!' : 'Registration successful. Welcome!');
            })
            .catch((error) => {
                console.error("Registration error:", error);
                if (error.code === 'auth/email-already-in-use') {
                    showNotification(currentLang === 'es' ? 'Este correo ya está registrado.' : 'This email is already in use.');
                } else {
                    showNotification(error.message);
                }
            });
    }
}

function subscribe(planName) {
    localStorage.setItem('c8l_subscription', planName);
    
    let planText = planName;
    if (planName === 'basic') planText = currentLang === 'es' ? 'Básico' : 'Basic';
    if (planName === 'premium') planText = currentLang === 'es' ? 'Premium' : 'Premium';
    if (planName === 'agency') planText = currentLang === 'es' ? 'Agencia' : 'Agency';

    showNotification(currentLang === 'es' ? 
        `¡Gracias! Suscripción al plan ${planText} realizada con éxito.` : 
        `Thank you! Subscription to ${planText} plan completed successfully.`);

    // If premium/agency/basic, grant credits and log in
    if (planName === 'basic') {
        localStorage.setItem('c8l_logged', 'true');
        localStorage.setItem('c8l_credits', '100');
    } else if (planName === 'premium') {
        localStorage.setItem('c8l_logged', 'true');
        localStorage.setItem('c8l_credits', '1000');
    } else if (planName === 'agency') {
        localStorage.setItem('c8l_logged', 'true');
        localStorage.setItem('c8l_credits', '9999'); // Agency has unlimited credits
    } else {
        localStorage.setItem('c8l_credits', '10');
    }
    
    updateCreditDisplay();

    setTimeout(() => {
        if (confirm(currentLang === 'es' ? '¿Quieres ir al Estudio VIP Corazones Locos?' : 'Do you want to go to the VIP Corazones Locos Studio?')) {
            window.location.href = 'users.html';
        }
    }, 1000);
}


