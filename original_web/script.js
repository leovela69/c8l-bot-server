// Neon Snake Game Logic
let snake, food, score, direction, gameInterval;
const canvas = document.getElementById('snakeGame');
const ctx = canvas ? canvas.getContext('2d') : null;
const box = 20;

function initSnakeGame() {
    if (!canvas) return;
    snake = [{ x: 10 * box, y: 10 * box }];
    direction = "RIGHT";
    score = 0;
    spawnFood();
    if (gameInterval) clearInterval(gameInterval);
    gameInterval = setInterval(drawSnake, 100);
    document.getElementById('game-msg').textContent = "";
}

function spawnFood() {
    food = {
        x: Math.floor(Math.random() * (canvas.width / box)) * box,
        y: Math.floor(Math.random() * (canvas.height / box)) * box
    };
}

function drawSnake() {
    ctx.fillStyle = "#0a0a0c";
    ctx.fillRect(0, 0, canvas.width, canvas.height);

    for (let i = 0; i < snake.length; i++) {
        ctx.fillStyle = i === 0 ? "#c5a059" : "rgba(197, 160, 89, 0.5)";
        ctx.fillRect(snake[i].x, snake[i].y, box, box);
        ctx.strokeStyle = "#0a0a0c";
        ctx.strokeRect(snake[i].x, snake[i].y, box, box);
    }

    ctx.fillStyle = "#b02a2a";
    ctx.fillRect(food.x, food.y, box, box);

    let snakeX = snake[0].x;
    let snakeY = snake[0].y;

    if (direction === "LEFT") snakeX -= box;
    if (direction === "UP") snakeY -= box;
    if (direction === "RIGHT") snakeX += box;
    if (direction === "DOWN") snakeY += box;

    if (snakeX === food.x && snakeY === food.y) {
        score++;
        document.getElementById('score-val').textContent = score;
        spawnFood();
    } else {
        snake.pop();
    }

    let newHead = { x: snakeX, y: snakeY };

    if (snakeX < 0 || snakeY < 0 || snakeX >= canvas.width || snakeY >= canvas.height || collision(newHead, snake)) {
        clearInterval(gameInterval);
        document.getElementById('game-msg').textContent = translations[currentLang]['game-over'];
        // Reward 1 credit for playing
        let credits = parseInt(localStorage.getItem('c8l_credits') || 0);
        localStorage.setItem('c8l_credits', credits + 1);
        updateCreditDisplay();
    }

    snake.unshift(newHead);
}

function collision(head, array) {
    for (let i = 0; i < array.length; i++) {
        if (head.x === array[i].x && head.y === array[i].y) return true;
    }
    return false;
}

document.addEventListener("keydown", (e) => {
    if (e.keyCode === 37 && direction !== "RIGHT") direction = "LEFT";
    else if (e.keyCode === 38 && direction !== "DOWN") direction = "UP";
    else if (e.keyCode === 39 && direction !== "LEFT") direction = "RIGHT";
    else if (e.keyCode === 40 && direction !== "UP") direction = "DOWN";
});

